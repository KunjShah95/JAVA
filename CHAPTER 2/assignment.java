public class assignment {

    public static void main(String[] args) {

        // Assignment Operator
        int a = 0;
        a += 5; // a = a + 5
        int b = 0;
        b -= 2; // b = b - 2
        int c = 1;
        c *= 3; // c = c * 3
        int d = 8;
        d /= 4; // d = d / 4

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
    }
}
